CREATE TABLE [dbo].[AUTHORS_BOOKS] (
    [AUTHOR_ID] INT NOT NULL,
    [BOOK_ID]   INT NOT NULL,
    CONSTRAINT [PK_AUTHORS_BOOKS] PRIMARY KEY CLUSTERED ([BOOK_ID] ASC, [AUTHOR_ID] ASC),
    CONSTRAINT [FK_AUTHORS_BOOKS_AUTHOR_ID] FOREIGN KEY ([AUTHOR_ID]) REFERENCES [dbo].[AUTHORS] ([ID]),
    CONSTRAINT [FK_AUTHORS_BOOKS_BOOK_ID] FOREIGN KEY ([BOOK_ID]) REFERENCES [dbo].[BOOKS] ([ID])
);

